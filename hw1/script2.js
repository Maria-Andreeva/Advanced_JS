/*Задание 2
Вы управляете рестораном, в котором работают разные повара, специализирующиеся на определенных блюдах. Клиенты приходят и делают заказы на разные блюда.

Необходимо создать систему управления этими заказами, которая позволит:

• Отслеживать, какой повар готовит какое блюдо.
• Записывать, какие блюда заказал каждый клиент.

Используйте коллекции Map для хранения блюд и их поваров, а также для хранения заказов каждого клиента. В качестве ключей для клиентов используйте объекты.

Повара и их специализации:

Виктор - специализация: Пицца.
Ольга - специализация: Суши.
Дмитрий - специализация: Десерты.*/

let cooking = new Map();

cooking.set("Пиццy", "Виктор");
cooking.set("Суши", "Ольга");
cooking.set("Десерты", "Дмитрий");

/*Блюда и их повара:

Пицца "Маргарита" - повар: Виктор.
Пицца "Пепперони" - повар: Виктор.
Суши "Филадельфия" - повар: Ольга.
Суши "Калифорния" - повар: Ольга.
Тирамису - повар: Дмитрий.
Чизкейк - повар: Дмитрий.*/

const viktorsCooking = new Set();
viktorsCooking.add('Пиццy "Маргарита"')
            .add('Пиццy "Пепперони"');

const olgaCooking = new Set();
olgaCooking.add('Суши "Филадельфия"')
            .add('Суши "Калифорния"');

const dmitriscooking = new Set();
dmitriscooking.add('Тирамису')
                .add('Чизкейк');

/*Заказы:

Клиент Алексей заказал: Пиццу "Пепперони" и Тирамису.
Клиент Мария заказала: Суши "Калифорния" и Пиццу "Маргарита".
Клиент Ирина заказала: Чизкейк.*/

let orders = new Map();
orders.set("Алексей", new Set(['Пиццy "Пепперони"', 'Тирамису']));
orders.set("Мария", new Set(['Суши "Калифорния"', 'Пиццy "Маргарита"']));
orders.set("Ирина", new Set(['Чизкейк']));

// Функции для работы с данными
function getChefByDish(dishName) {
    if (viktorsCooking.has(dishName)) return cooking.get("Пиццy");
    if (olgaCooking.has(dishName)) return cooking.get("Суши");
    if (dmitriscooking.has(dishName)) return cooking.get("Десерты");
    return "Блюдо не найдено";
}

// Проверка: какой повар готовит конкретное блюдо
console.log('Ответсвенный повар:');
console.log(`Пиццу "Пепперони": ${getChefByDish('Пиццy "Пепперони"')}`);
console.log(`Тирамису: ${getChefByDish('Тирамису')}`);
console.log(`Суши "Филадельфия": ${getChefByDish('Суши "Филадельфия"')}`);

// Проверка: что заказал каждый клиент
console.log('\nЗаказы клиентов:');
for (const [client, dishes] of orders) {
    console.log(`Клиент ${client} заказал(а): ${[...dishes].join(', ')}`);
}